 <br>
 <h2>CATEDRAL</h2>
 <br>
 <img src="Imagen/iglesia-catedral.jpg" alt="">
 <br>
 <br>
 <p>Más de 400 años allí. El templo máximo de la Córdoba de las Campanas es la Iglesia Catedral.</p>
 <p> Enclavada siempre en el mismo lugar, es el centro del casco histórico, escoltada por muchos otros templos.</p>
 <p>Permanece estoica e imponente al lado de la Plaza San Martín y con la réplica del histórico Cabildo a su lado.</p>
 <p>De colores amarillos pálidos que cobran calidez con el sol del atardecer, reúne en su aspecto varios estilos arquitectónicos.</p>
 <br>
