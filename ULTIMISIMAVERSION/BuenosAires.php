 <h2>Jardin Japones</h2>
 <br>
 <img src="Imagen/jardinjapones.jpg" alt="">
 <br>
 <br>
 <p>Rodeado por los Bosques de Palermo, el Jardín Japonés es un rincón de Buenos Aires que enamora a quien lo visite. </p>
 <p>Construido en 1967 en ocasión de la visita del emperador de Japón Akihito y su esposa Michiko,</p>
 <p>se trata del jardín de estilo japonés más grande fuera del país nipón.</p>
 <p>Caminar entornado por bonsais, azaleas, kokedamas, orquídeas y faroles de cemento es un placer inolvidable.</p>
 <p>Tiene también un Chashitsu (casa de té): un espacio construido especialmente para llevar a cabo la tradicional ceremonia del té japonesa.</p>
 <p>Ornamentado con elementos tradicionales, cuyo interior fue importado desde Japón, </p>
 <p>algunos de sus componentes datan de hasta más de cien años de antigüedad.</p>
 <p>Su exquisito interior se encuentra confeccionado de forma artesanal. Una sala de arte y un restaurante completan la visita.</p>
 <p>Tomando como modelo el Jardín Zen, en 1977 fue rediseñado bajo la dirección del ingeniero paisajista Yasuo Inomata. </p>
 <p>En el parque también hay un edificio que alberga un centro cultural; un restaurante de cocina japonesa;</p>
 <p>un vivero donde se pueden comprar plantas (bonsai) y alimento para los peces del lago; y una tienda con artesanías japonesas.</p>

