 <h2>Planetario</h2>
 <br>
 <img src="Imagen/planetario.jpg" alt="">
 <br>
 <p>El Planetario es el principal centro de divulgación de astronomía de la ciudad. </p>
 <p>Cuenta con una sala de proyección semiesférica con trescientas sesenta butacas reclinables </p>
 <p>y una cúpula de veinte metros de diámetro donde se reproducen cerca de 8.900 estrellas, planetas y satélites del universo,</p>
 <p>gracias a un imponente equipo (único en Latinoamérica) que contiene seis proyectores Sky-Skan con resolución 8K,</p>
 <p>lo que indica que el domo contiene 38 millones de píxeles.</p>
 <br>
 <p>Su edificio de cinco pisos también cuenta con un museo y una pequeña sala de proyección secundaria, </p>
 <p>además de albergar una colección de meteoritos provenientes del norte argentino. En el lago adyacente se encuentra la escultura Sorprendida,</p>
 <p>del italiano Nicolás A. Ferrari y, a pocos metros, un monolito evocativo del sabio polaco Nicolás Copérnico.</p>
 <br>
 <a href="https://turismo.buenosaires.gob.ar/es/otros-establecimientos/planetario-galileo-galilei">Puedes ver más del planetario aqui</a>
